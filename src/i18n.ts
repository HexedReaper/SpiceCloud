// ── Locale detection ──────────────────────────────────────────────────────────

function detectLang(): string {
  // 1. Spotify/Spicetify sets <html lang="xx"> to the user's chosen language.
  try {
    const h = document.documentElement.lang;
    if (h) return h.split(/[-_]/)[0].toLowerCase();
  } catch {}
  // 2. Spicetify locale API (varies by version).
  try {
    const sl = (
      Spicetify as unknown as { Locale?: { getLocale?(): string } }
    ).Locale?.getLocale?.();
    if (sl) return sl.split(/[-_]/)[0].toLowerCase();
  } catch {}
  // 3. Browser language.
  return (navigator.language || "en").split(/[-_]/)[0].toLowerCase();
}

let _lang: string | null = null;

// ── Public API ────────────────────────────────────────────────────────────────

/** Translate a key.  Extra args are forwarded to string-builder values. */
export function t(key: string, ...args: string[]): string {
  if (!_lang) _lang = detectLang();
  const row = TRANSLATIONS[_lang] ?? TRANSLATIONS["en"];
  const val = row[key] ?? TRANSLATIONS["en"][key] ?? key;
  if (typeof val === "function")
    return (val as (...a: string[]) => string)(...args);
  return val as string;
}

/** Render a string that contains {KEY} placeholders as <kbd> elements. */
export function tWithKbd(key: string): Array<string | { kbd: string }> {
  const raw = t(key);
  return raw.split(/(\{[^}]+\})/).map((part) => {
    const m = part.match(/^\{(.+)\}$/);
    return m ? { kbd: m[1] } : part;
  });
}
